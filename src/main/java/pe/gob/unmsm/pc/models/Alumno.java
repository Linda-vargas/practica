package pe.gob.unmsm.pc.models;

public class Alumno {
	private String codalu;
	private String nomalu;
	private String apealu;
	
	public String getCodalu() {
		return codalu;
	}
	public void setCodalu(String codalu) {
		this.codalu = codalu;
	}
	public String getNomalu() {
		return nomalu;
	}
	public void setNomalu(String nomalu) {
		this.nomalu = nomalu;
	}
	public String getApealu() {
		return apealu;
	}
	public void setApealu(String apealu) {
		this.apealu = apealu;
	}

	
	
	
	
}
